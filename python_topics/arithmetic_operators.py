# Arithmetic operators are used with numeric values to
# perform common mathematical operations.

number_1 = 10
number_2 = 20

# sum
print(number_1 + number_2)
print(number_1 + 3.5)

# subtraction
print(number_1 - number_2)
print(number_2 - 3.5)

# multiplication
print(number_1 * number_2)
print(number_1 * 1.7)

# division
print(number_1 / number_2)
print(number_2 // number_1)

# modulo
print(number_1 % number_2)

# power
print(number_1 ** 2)

# parenthesis
answer_1 = 10 + 5 * 4
answer_2 = (10 + 5) * 4
print(answer_1, answer_2) 
