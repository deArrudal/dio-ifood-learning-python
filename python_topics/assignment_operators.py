# Assignment Operators are used to assigning values to variables.

number = 100
print(number)

# add and assign.
number += 90 
print(number)

# subtract and assign.
number -= 10 
print(number)

# multiply and assign.
number *= 2 
print(number)

# division and assign.
number /= 4 
print(number)

# modulus and assign.
number %= 12 
print(number)

# multiply and assign.
number **= 2
print(number)

# integer division and assign.
number = int(number)
number //= 18
print(number)

