# Specify a Variable Type using cast.

# int() - constructs an integer number.
number = 1.789
print(number)
print(int(number))

# float() - constructs a float number.
number = 10
print(number)
print(float(number))
# str() - constructs a string.
print(type(number))
number = str(number)
print(type(number))

# implicit casting
print(100 / 2) # division
print(100 // 2) # integer division