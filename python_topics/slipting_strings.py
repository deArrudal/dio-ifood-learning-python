sample = "Yellow is a bright color"

# display the first character.
print(sample[0])
# display the last character.
print(sample[-1])
# display the first ten characters.
print(sample[:10])
# display starting from the tenth character.
print(sample[10:])
# display the characters with index between 10 and 20.
print(sample[10:20])  
# display the characters with index between 10 and 20 with step 2.
print(sample[10:20:2])
# display the whole sample.
print(sample[:])
# display the sample as reverse.
print(sample[::-1])
