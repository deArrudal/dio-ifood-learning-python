# Comparison operators are used to compare two values.

number_1 = 200
number_2 = 400

# equal.
print(number_1 == (number_2 / 2))

# not equal.
print(number_1 != number_2)

# greater than.
print(number_1 > number_2)

# less than.
print(number_1 < number_2)

# greater or equal.
print(number_1 >= number_2)

# less or equal.
print(number_1 <= number_2)